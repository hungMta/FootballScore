public class Away {
    private int goals;
    private int goalsAgainst;
    private int wins;
    private int draws;
    private int losses;

    public int getGoals() {
        return goals;
    }

    public int getGoalsAgainst() {
        return goalsAgainst;
    }

    public int getWins() {
        return wins;
    }

    public int getDraws() {
        return draws;
    }

    public int getLosses() {
        return losses;
    }
}
